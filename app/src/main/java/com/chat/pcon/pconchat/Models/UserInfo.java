package com.chat.pcon.pconchat.Models;

public class UserInfo {
    public String name;
    public String email;
    public String color;
    public String uid;



    public UserInfo(){}

    public UserInfo(String name,String email,String color,String uid){
        this.name = name;
        this.email = email;
        this.color = color;
        this.uid = uid;
    }


}
